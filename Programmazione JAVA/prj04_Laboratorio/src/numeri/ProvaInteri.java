package numeri;

public class ProvaInteri {

	public static void main(String[] args) {
		byte b = 1;//8bit
		short s = 2;//18bit
		int i =3;//32bit
		long lo=4;//64bit
		int somma=b+s+i;
		System.out.println(b);
		System.out.println(s);//crtl+space ctrl+alt+freccia giu
		System.out.println(i);
		System.out.println(lo);
		System.out.println("Il valore di somma: "+somma);
		
	}

}
